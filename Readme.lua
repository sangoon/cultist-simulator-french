-------------

* Installation sous Windows, copier le repertoire Traduction dans:


C:\Utilisateurs\...\AppData\LocalLow\Weather Factory\Cultist Simulator\mods

(Il s'agit d'un dossier caché à faire apparaitre dans les options: onglet Affichage, Options, onglet Affichage et cocher afficher les fichiers, dossiers cachés puis valider)

Ensuite copier le fichier Strings.csv dans:

...:\Steam\steamapps\common\Cultist Simulator\cultistsimulator_Data\StreamingAssets\content


* Installation sous Linux, copier le repertoire Traduction dans:

/home/USERNAME/.config/unity3d/Weather Factory/Cultist Simulator/mods/


Ensuite copier le fichier Strings.csv dans:

...:\Steam\steamapps\common\Cultist Simulator\cultistsimulator_Data\StreamingAssets\content


* Installation sous Mac, copier le repertoire Traduction dans:

Bibliothèque ▸ Application Support ▸ biz.weatherfactory.cultistsimulator ▸ Mods

Ensuite copier le fichier Strings.csv dans:

Bibliothèque ▸ Application Support ▸ Steam ▸ SteamApps ▸ common ▸ Cultist Simulator ▸ Content


-------------

Lancer le jeu et activer le mod dans l'onglet DLC en haut à droite
Important, il est necessaire de relancer le jeu apres avoir chargé la traduction


